#count() : In python, the count() function is used to count thhe number of times an element appears in the list. If the element is not present in the list, it returns 0.
num = [1,2,3,4,3,2,2,1,4,5,8]
cnt=num.count(2)
print("The count of 2 in the list num is:", cnt)
cnt=num.count(10)
print("The count of 10 in the list num is:", cnt)