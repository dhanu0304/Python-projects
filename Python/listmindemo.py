#min(): In python the min() function is used to find the minimum value in the list.
#For string items, the string with lower ASCII value is considered as minimum. For example, 'a' is less than 'b' because the ASCII value of 'a' is 97 and the ASCII value of 'b' is 98.
num= [10, 20, 30, 40, 50]
lang = ['java',  'c','python', 'cpp','sql', 'SQL']
print("Min of num:", min(num))
print("Min of lang:", min(lang))