#max() : In python, the max() function is used to find the maximum value from a list or any iterable. It can also be used to find the maximum of two or more arguments.
#For string items, the string with higher ASCII value is considered as maximum. For example, 'b' is greater than 'a' because the ASCII value of 'b' is 98 and the ASCII value of 'a' is 97.
list1=[10, 20, 30, 40, 50]
list2=['java',  'c','python', 'cpp','sql', 'SQL']
print("Max of list1:", max(list1))
print("Max of list2:", max(list2))